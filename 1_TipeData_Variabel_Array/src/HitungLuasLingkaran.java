/*
Tulislah sebuah program yang menggunakan variable double
untuk menghitung luas sebuah lingkaran
*/
public class HitungLuasLingkaran {
    public static void main (String args[]){
        double pi, r, a;
        r = 10.8;
        pi = 3.1416;
        a = pi * r * r;
        System.out.println("Luas Lingkaran adalah : "+a);
    }
}
