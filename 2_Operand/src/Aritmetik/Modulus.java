/*
Tunjukan suatu program yang memuat sebuah operator modulus (%)
yang menghasilkan sisa dari operasi pembagian. 
Program juga harus bisa diterapkan pada tipe titik mengambang
*/
public class Modulus {
    public static void main(String args[]){
        int x = 42;
        double y =42.25;
        
        System.out.println("x mod 10 = "+ x % 10);
        System.out.println("y mod 10 = "+ y % 10);
    }
}
